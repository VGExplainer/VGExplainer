import os
import json
import re
import networkx as nx
from tqdm import tqdm

class JoernNode2LineConverter:
    def __init__(self, dot_dir, in_res_dir, out_res_dir):
        """
        Args:
            dot_dir: Path to the flat directory containing original .dot files.
            in_res_dir: Path to the flat directory containing JSON files generated by the explainer.
            out_res_dir: Root directory for the converted output results.
        """
        self.dot_dir = dot_dir
        self.in_res_dir = in_res_dir
        self.out_res_dir = out_res_dir
        
        # Matches labels containing line numbers in Joern dot graphs.
        self.line_regex = re.compile(r'(\d+)\s*<[bB][rR]', re.IGNORECASE)

    def build_node_to_line_map(self, dot_path):
        """Builds a mapping of Joern Node ID (string) -> Line Number (integer)."""
        try:
            pdg = nx.nx_agraph.read_dot(dot_path)
        except Exception as e:
            print(f"Graph read error: {dot_path} -> {e}")
            return {}

        node2line = {}
        for node in pdg.nodes():
            label = pdg.nodes[node].get('label', '')
            match = self.line_regex.search(label)
            if match:
                node2line[str(node)] = int(match.group(1))
        return node2line

    def convert_results(self):
        # 🌟 Define target folder names and their corresponding truncation ratios.
        ratio_map = {
            "20": 1,
            "15": 0.75,
            "10": 0.5,
            "5": 0.25
        }
        
        # Pre-create the 4 subdirectories in the output root directory.
        for folder in ratio_map.keys():
            os.makedirs(os.path.join(self.out_res_dir, folder), exist_ok=True)
            
        # Flat read: only read all JSON files in the current directory.
        json_files = [f for f in os.listdir(self.in_res_dir) if f.endswith('.json')]
        
        print(f"📁 Found {len(json_files)} JSON files to process...")
        
        for file in tqdm(json_files):
            base_name = file.replace('.json', '')
            dot_path = os.path.join(self.dot_dir, f"{base_name}.dot")
            
            if not os.path.exists(dot_path):
                print(f"⚠️ Corresponding dot graph not found: {dot_path}, skipping file {file}")
                continue
                
            # 1. Establish the mapping table for the current graph.
            node2line_map = self.build_node_to_line_map(dot_path)
            if not node2line_map:
                continue
            
            # 2. Read the JSON result.
            json_path = os.path.join(self.in_res_dir, file)
            with open(json_path, 'r', encoding='utf-8') as f:
                try:
                    res_data = json.load(f)
                except json.JSONDecodeError:
                    print(f"❌ JSON parsing failed: {json_path}")
                    continue
            
            # 3. Intelligently extract Joern node list.
            joern_nodes = []
            if isinstance(res_data, list):
                joern_nodes = res_data 
            elif isinstance(res_data, dict):
                for key, val in res_data.items():
                    if isinstance(val, list):
                        joern_nodes = val
                        break
            
            if not joern_nodes:
                continue
            
            total_nodes = len(joern_nodes)

            # 4. Iterate through four ratios, generating independent JSON data for each.
            for folder_name, ratio in ratio_map.items():
                # Calculate truncation position (keep at least 1 node).
                k = max(1, int(total_nodes * ratio)) if ratio > 0 else 0
                selected_nodes = joern_nodes[:k]
                
                important_lines = set() # Automatic deduplication.
                for node_id in selected_nodes:
                    line_num = node2line_map.get(str(node_id))
                    if line_num is not None:
                        important_lines.add(line_num)
                
                sorted_lines = sorted(list(important_lines))
                
                # Assemble multi-precision JSON data.
                if isinstance(selected_nodes, list):
                    new_res_data = {
                        "original_nodes": selected_nodes,
                        "important_lines": sorted_lines
                    }
                else:
                    new_res_data = res_data.copy()
                    new_res_data['important_lines'] = sorted_lines

                # 🌟 5. Save results into the corresponding subfolders (20, 15, 10, 5).
                out_json_path = os.path.join(self.out_res_dir, folder_name, file)
                with open(out_json_path, 'w', encoding='utf-8') as f:
                    json.dump(new_res_data, f, indent=4)

